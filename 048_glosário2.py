glossário = {
        'print':'mostra coisas na tela',
        'int':'armazena valores inteiros',
        'float':'armazena valores com um ponto flutuante',
        'boolean':'armazena os valores True ou False',
        'if-elif-else':'termos usados para condições',
        'items':'função built-in do dicionário, mostra todos os pares chave-valor',
        'keys':'representa a parte chave em pares chave-valor nos dicionários',
        'values':'representa a parte valor em pares chave-valor nos dicionários',
        'get':'método dos dicionários, verifica se certa chave está presente no dicionário',
        'set':'método dos dicionários, identifica os termos únicos da coleção e cria um conjunto a partir deles'
}
for p, s in glossário.items():
    print(f'{p}:\n\t{s}')
