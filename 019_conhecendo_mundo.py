lugares = ['Marrocos', 'Japan', 'French', 'New Zealand', 'Brazil', 'Africa']
print(lugares) #lista original
print(sorted(lugares)) # lista ordenada
print(lugares) # a ordem original não foi alterada
lugares.reverse() # ordem original foi alterada
print(lugares)
lugares.sort() # organizada alfabéticamente, perdeu a ordem original
print(lugares) 
lugares.sort(reverse=True) # ordem alfabética inversa
print(lugares)
